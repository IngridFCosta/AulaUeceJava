package Aula08;

/**
 *
 *Usando conceitos de arrays, faça um programa que dado um conjunto de 5 números,
 * imprima o que está na primeira posição, na do meio e na do final?

 */
public class Exercicio08 {
    public static void main(String[] args) {
        int [] numeros={1,2,3,4,5};
       
        System.out.println("Primeira posição: "+numeros[0]);
        System.out.println("Posição do meio: "+numeros[2]);
        System.out.println("Ultima posição: "+numeros[4]);
        
    }
    
}
